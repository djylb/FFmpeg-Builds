#!/bin/bash

SCRIPT_REPO="https://github.com/alsa-project/alsa-lib.git"
SCRIPT_COMMIT="master"

ffbuild_depends() {
    echo base
}

ffbuild_enabled() {
    [[ $TARGET == linux* ]] || return 1
    return 0
}

ffbuild_dockerdl() {
    echo "git clone --filter=blob:none --depth=1 --branch \"$SCRIPT_COMMIT\" \"$SCRIPT_REPO\" ."
}

ffbuild_dockerbuild() {
    set -e

    if [[ -x ./gitcompile ]]; then
        ./gitcompile
    elif [[ -x ./autogen.sh ]]; then
        ./autogen.sh
    else
        autoreconf -fiv
    fi

    local myconf=(
        --prefix="$FFBUILD_PREFIX"
        --disable-shared
        --enable-static
    )

    if [[ $TARGET == linux* ]]; then
        myconf+=( --host="$FFBUILD_TOOLCHAIN" )
    else
        echo "Unknown target"
        return -1
    fi

    ./configure "${myconf[@]}"
    make -j"$(nproc)"
    make install DESTDIR="$FFBUILD_DESTDIR"
}

ffbuild_configure() {
    echo --enable-alsa
}

ffbuild_unconfigure() {
    echo --disable-alsa
}